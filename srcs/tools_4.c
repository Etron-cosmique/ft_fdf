#include "fdf.h"

void	printab(t_fdf *f)
{
	int	x;
	int y;

	x = 0;
	y = 0;
	while (y < f->m.size_y)
	{
		x = 0;
		while (x < f->m.size_x)
		{
			printf("%d ", f->tab[y][x]);
			x++;
		}
		y++;
		printf("\n");
	}
}